public class Mediaa {
    public double calc(double y, double x){
        return (x+y)/2;
    }
    public double calc(double x, double y, double z){
        return (z+y+x)/3;
    }
    public double calc(double x, double y, double z, double w){
        return (y+z+w+x)/4;
    }
}
